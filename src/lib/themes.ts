export type ThemeName = keyof typeof themes;
export const themeNames = Object.keys(themes) as ThemeName[];

const SIDEBAR_MAP: Record<string, typeof THEME_VARIABLES[number]> = {
  "sidebar-background": "background",
  "sidebar-foreground": "foreground",
  "sidebar-primary": "primary",
  "sidebar-primary-foreground": "primary-foreground",
  "sidebar-accent": "accent",
  "sidebar-accent-foreground": "accent-foreground",
  "sidebar-border": "border",
  "sidebar-ring": "ring",
};

export function applyTheme(themeName: ThemeName, mode: "light" | "dark") {
  const theme = themes[themeName];
  if (!theme) return;
  const vars = theme[mode];
  const root = document.documentElement;
  for (const key of THEME_VARIABLES) {
    root.style.setProperty(`--${key}`, vars[key]);
  }
  for (const [sidebarVar, themeVar] of Object.entries(SIDEBAR_MAP)) {
    root.style.setProperty(`--${sidebarVar}`, vars[themeVar]);
  }
}

export function getStoredTheme(): ThemeName {
  try {
    const stored = localStorage.getItem(THEME_STORAGE_KEY);
    if (stored && stored in themes) return stored as ThemeName;
  } catch {}
  return DEFAULT_THEME;
}

export function storeTheme(themeName: ThemeName) {
  try {
    localStorage.setItem(THEME_STORAGE_KEY, themeName);
  } catch {}
}
